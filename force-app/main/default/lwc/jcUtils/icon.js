const ICON_MAP = new Map([
	['action', ACTION],
	['custom', CUSTOM],
	['doctype', DOCTYPE],
	['standard', STANDARD],
	['utility', UTILITY]
]);
const iconCategories = () => {
	return Array.from(ICON_MAP.keys());
};
const iconsOfCategory = (category) => {
	return ICON_MAP?.get(category);
};
const randomIconCategory = () => {
	return Array.from(ICON_MAP.keys())[Math.floor(Math.random() * ICON_MAP.size)];
};

const parseIconString = (string) => {
	let icon = {
		valid: false,
		original: string
	};
	let match = string?.toLowerCase()?.match(/([a-zA-Z]+):([a-zA-Z_0-9]+)/);
	if (match) {
		[icon.formatted, icon.category, icon.name] = match;
		if (icon.category === 'random' || icon.name === 'random') {
			// eslint-disable-next-line no-use-before-define
			icon = randomIcon(icon.category);
		} else {
			icon.valid = !!iconsOfCategory(icon.category)?.includes(icon.name);
		}
	}
	return icon;
};
const randomIcon = (category) => {
	category = ICON_MAP.has(category?.toLowerCase()) ? category?.toLowerCase() : randomIconCategory();
	const iconList = iconsOfCategory(category);
	return parseIconString(category + ':' + iconList[Math.floor(Math.random() * iconList?.length)]);
};

export { parseIconString, iconCategories, iconsOfCategory, randomIconCategory, randomIcon };
